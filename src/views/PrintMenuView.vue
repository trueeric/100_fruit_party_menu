                >{{ addon.name }} <span style="color: #e63946">+${{ addon.price }}</span></label
              >
            </div>
          </div>

          <h3>特殊需求</h3>
          <p>• 甜度調整：正常 / 少糖 / 半糖 / 微糖 / 無糖</p>
